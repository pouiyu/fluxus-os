; ============================================
; boot.asm - Fluxus OS 引导扇区
; 功能：设置 VGA 13h 图形模式、加载内核、进入保护模式并跳转至 C 入口
; 语法：NASM 16/32 位混合
; ============================================

[ORG 0x7C00]                  ; BIOS 将引导扇区加载到 0x7C00
[BITS 16]                     ; 实模式 16 位代码

; ----------------------------
; 引导扇区入口
; ----------------------------
start:
    cld                       ; 清除方向标志，字符串操作正向
    xor ax, ax                ; 设置段寄存器为 0
    mov ds, ax
    mov es, ax
    mov ss, ax
    mov sp, 0x7C00            ; 栈向下生长（0x7C00 以下可用空间）

    mov [boot_drive], dl      ; 保存 BIOS 传入的启动驱动器号

    ; ---------- 设置 VGA 13h 图形模式 (320x200, 256色) ----------
    mov ax, 0x0013            ; AH=00h (设置视频模式), AL=13h (320x200 256色)
    int 0x10                  ; 调用 BIOS 视频中断

    ; ---------- 从磁盘加载内核到内存 0x8000 ----------
    mov ah, 0x02              ; BIOS 读扇区功能号
    mov al, KERNEL_SECTORS    ; 要读取的扇区数
    mov ch, 0x00              ; 柱面号低 8 位 = 0
    mov cl, 0x02              ; 起始扇区号 = 2（扇区 1 是引导扇区之后）
    mov dh, 0x00              ; 磁头号 = 0
    mov dl, [boot_drive]      ; 驱动器号
    mov bx, 0x8000            ; ES:BX = 目标缓冲区地址
    int 0x13                  ; 调用 BIOS 磁盘中断
    jc  disk_error            ; CF=1 表示读取失败

    ; ---------- 启用 A20 地址线（访问 1MB 以上内存） ----------
    in   al, 0x92             ; 读取系统控制端口 A
    or   al, 0x02             ; 设置 A20 门控位
    out  0x92, al             ; 写回端口

    ; ---------- 禁止可屏蔽中断 ----------
    cli

    ; ---------- 加载全局描述符表 ----------
    lgdt [gdt_descriptor]

    ; ---------- 进入保护模式 ----------
    mov eax, cr0              ; 读取控制寄存器 CR0
    or  al, 0x01              ; 设置 PE (Protection Enable) 位
    mov cr0, eax              ; 写回 CR0，CPU 进入保护模式

    ; 远跳转以清空指令预取队列，并加载 32 位代码段选择子
    jmp 0x08:protected_mode

    ; ---------- 磁盘读取错误处理 ----------
disk_error:
    mov si, disk_error_msg
    call print_string_16
    jmp $                     ; 死循环

; ---------- 16 位实模式字符串打印 (BIOS) ----------
print_string_16:
    lodsb                     ; 加载 DS:SI 指向的字符到 AL
    or  al, al                ; 检查是否遇到字符串结束符
    jz  .done
    mov ah, 0x0E              ; BIOS 电传打字功能
    int 0x10
    jmp print_string_16
.done:
    ret

; ============================================
; 32 位保护模式入口
; ============================================
[BITS 32]
protected_mode:
    ; 设置数据段选择子为 0x10（平坦内存模型数据段）
    mov ax, 0x10
    mov ds, ax
    mov es, ax
    mov fs, ax
    mov gs, ax
    mov ss, ax
    mov esp, 0x90000          ; 设置 32 位内核栈顶

    ; ---------- 跳转到 C 内核入口 ----------
    call 0x08:0x8000          ; 调用 kernel_main（物理地址 0x8000）

    ; 如果内核意外返回，则停机
    cli
    hlt

; ============================================
; 全局描述符表 (GDT)
; 平坦内存模型：4GB 地址空间，基址 0，段界限 4GB
; ============================================
gdt_start:
    ; 第 0 项：空描述符（强制）
    dq 0x0000000000000000

    ; 第 1 项 (选择子 0x08)：32 位内核代码段
    ; 基址=0, 界限=0xFFFFF, DPL=0, 代码段, 32 位, 4KB 粒度
    dw 0xFFFF                 ; 段界限 [15:0]
    dw 0x0000                 ; 基址 [15:0]
    db 0x00                   ; 基址 [23:16]
    db 10011010b              ; P=1, DPL=00, S=1, Type=1010 (代码/可执行/可读)
    db 11001111b              ; G=1(4KB), D/B=1(32位), L=0, AVL=0, 段界限[19:16]=1111
    db 0x00                   ; 基址 [31:24]

    ; 第 2 项 (选择子 0x10)：32 位内核数据段
    ; 基址=0, 界限=0xFFFFF, DPL=0, 数据段, 32 位, 4KB 粒度
    dw 0xFFFF                 ; 段界限 [15:0]
    dw 0x0000                 ; 基址 [15:0]
    db 0x00                   ; 基址 [23:16]
    db 10010010b              ; P=1, DPL=00, S=1, Type=0010 (数据/可读写)
    db 11001111b              ; G=1(4KB), D/B=1(32位), L=0, AVL=0, 段界限[19:16]=1111
    db 0x00                   ; 基址 [31:24]
gdt_end:

; GDT 描述符结构（lgdt 使用）
gdt_descriptor:
    dw gdt_end - gdt_start - 1  ; GDT 大小 (16 位)
    dd gdt_start                 ; GDT 基址 (32 位)

; ----------------------------
; 数据区
; ----------------------------
boot_drive:      db 0
disk_error_msg:  db "ERR: Disk read failed!", 0

; 内核扇区数：每个扇区 512 字节，此处预留 64 扇区 (32KB) 空间
KERNEL_SECTORS equ 64

; ----------------------------
; 填充至 510 字节，添加引导扇区签名
; ----------------------------
times 510 - ($ - $$) db 0
dw 0xAA55                     ; 引导扇区魔数
